#pragma platform(VEX2)
#pragma competitionControl(Competition)

#include "Vex_Competition_Includes.c"
#include "functions.h"

void pre_auton(){
	bStopTasksBetweenModes = true;
	initialize();
}

task autonomous(){
	move(f,360,127);
	lift(47,127);
	intake(10,127);
	hold(kArm,127);
	getSensor(kEncoderLeft);
	while(true){
		move(f,360,127);	//Move base forward
		lift(90, 127);	//Move arm up
		intake(80,127);	//Open claw
		if(baseDone & armDone & clawDone) break;	//Look if processes are done and break from look if finished
	}
	resetValues();
}

task usercontrol(){
	while(true){
		driverControl();
	}
}
